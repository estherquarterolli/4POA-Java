package controller;

public class Doce {
    public String nome;
    public double preco;
    public String categoria;
    public String validade;

    public Doce(String nome, double preco, String categoria, String validade) {
        this.nome = nome;
        this.preco = preco;
        this.categoria = categoria;
        this.validade = validade;
    }
}