package model;

public class Doce {
    public String nome;
    public double preco;
    public String tipo;
    public String validade;

    public Doce(String nome, double preco, String tipo, String validade) {
        this.nome = nome;
        this.preco = preco;
        this.tipo = tipo;
        this.validade = validade;
    }
}